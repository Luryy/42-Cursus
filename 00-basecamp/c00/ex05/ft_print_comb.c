#include <unistd.h>

void	print_combo_numbers(int *n)
{
	if (n[1] < n[2])
	{
		write(1, &n[0], 1);
		write(1, &n[1], 1);
		write(1, &n[2], 1);
		if (!((n[0] == 55) && (n[1] == 56) && (n[2] == 57)))
		{
			write(1, ", ", 2);
		}
	}
}

void	ft_print_comb(void)
{
	int n[3];

	n[0] = 47;
	n[1] = 48;
	n[2] = 49;
	while (++n[0] <= 57)
	{
		while (++n[1] <= 57)
		{
			if (n[0] < n[1])
			{
				while (++n[2] <= 57)
				{
					print_combo_numbers(n);
				}
			}
			n[2] = 49;
		}
		n[1] = 48;
	}
}
