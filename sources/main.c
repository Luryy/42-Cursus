#include <minishell.h>

int	main(int argc, char **argv, char **envp)
{
	char	*line;

	(void)argc;
	(void)argv;
	(void)envp;
	while (1)
	{
		line = readline("minishell > ");
		add_history(line);
		ft_execute(line);
	}
	return (0);
}
